//! Schema migration set for `main.db`, wired to the shared migration runner.
//!
//! The set of migrations is discovered at build time by `build.rs`, which scans
//! `sql/*.sql`, verifies the files form a gapless `0..N` sequence, and embeds them
//! into the binary as [`EMBEDDED_MIGRATIONS`]. Nothing about the migration list is
//! hand-maintained in source — adding a migration means dropping a new numbered
//! `.sql` file into `sql/`.
//!
//! The generic runner that applies and integrity-checks a migration set —
//! [`run`], [`EmbeddedMigration`], `user_version`, and the tracking/adoption logic
//! — lives in the shared `kls-web-core` crate (see DASHBOARD_DECOUPLING_PLAN.md,
//! Phase 3) and is re-exported here so existing `crate::migrations::…` call sites
//! (including the request-log migration set in `logging.rs`) are unchanged. Only
//! the build-time-embedded set and the `main.db` entry points stay app-side.

pub use kls_web_core::migrations::*;

// Pulls in `pub const EMBEDDED_MIGRATIONS: [EmbeddedMigration; N]` generated by
// build.rs. Kept as an `include!` so the array literal is type-checked against the
// `EmbeddedMigration` struct re-exported above.
include!(concat!(env!("OUT_DIR"), "/migrations_generated.rs"));

/// Name of the table tracking applied migrations and their checksums.
const TRACKING_TABLE: &str = "schema_migrations";

/// The version `main.db` reaches once every embedded migration is applied, equal to
/// the migration count.
pub fn target_version() -> u32 {
    EMBEDDED_MIGRATIONS.len() as u32
}

/// Runs all pending `main.db` migrations and verifies the integrity of applied ones.
///
/// This is the function handed to the connection pool's `with_init`. It assumes the
/// pool has already configured connection-level pragmas (`busy_timeout`,
/// `journal_mode=WAL`, …) so concurrent invocations can serialise on the write lock
/// rather than failing with `SQLITE_BUSY`.
pub fn migrate(conn: &mut rusqlite::Connection) -> anyhow::Result<()> {
    run(conn, &EMBEDDED_MIGRATIONS, TRACKING_TABLE)
}

#[cfg(test)]
mod tests {
    use super::*;

    fn open_memory() -> rusqlite::Connection {
        let conn = rusqlite::Connection::open_in_memory().expect("open in-memory db");
        conn.busy_timeout(std::time::Duration::from_secs(5)).unwrap();
        conn
    }

    #[test]
    fn embedded_migrations_are_contiguous() {
        // build.rs enforces this, but assert at runtime too as a guard.
        for (index, migration) in EMBEDDED_MIGRATIONS.iter().enumerate() {
            assert_eq!(
                migration.version as usize, index,
                "migration {index} is out of sequence"
            );
        }
        assert!(
            !EMBEDDED_MIGRATIONS.is_empty(),
            "expected at least one embedded migration"
        );
    }

    #[test]
    fn migrate_fresh_database_reaches_target() {
        let mut conn = open_memory();
        migrate(&mut conn).expect("migrate fresh db");
        assert_eq!(user_version(&conn).unwrap(), target_version());

        // Every migration recorded with a checksum.
        let count: u32 = conn
            .query_row(&format!("SELECT COUNT(*) FROM {TRACKING_TABLE}"), [], |r| r.get(0))
            .unwrap();
        assert_eq!(count, target_version());
    }

    #[test]
    fn migrate_is_idempotent() {
        let mut conn = open_memory();
        migrate(&mut conn).expect("first migrate");
        // A second run must be a no-op and must not error.
        migrate(&mut conn).expect("second migrate");
        assert_eq!(user_version(&conn).unwrap(), target_version());
    }
}
